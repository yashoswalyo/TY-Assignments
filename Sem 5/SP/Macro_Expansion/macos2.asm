MACRO
CLRMEM &X,&N,&REG=BREG 
LCL &M
&S SET 0
MOVER &REG = '0'
.MORE MOVEM &REG,&X + &S
&S SET &S + 1
AIF (&M NE &N ) .MORE
MEND
MACRO
DOMATH &Q,&W,&R=AREG,&KEY=CREG
LCL &V
&V SET 0
MOVER &R = '0'
.ADD MOVEM &R,&Q + &V
&V SET &V + 1
AIF (&V NE &W ) .ADD
MEND
MACRO
DESCOR &D,&E
MOVER BREG,&D
SUB BREG,&E
MOVEM BREG,&D
MEND
